Money = int(input("เงินที่ฉันได้พร้อมกับโบนัส 20 บาท(หลังจากใช้เงิน) เงินที่ได้เท่ากับ : "))
while Money > 100 :
    print("ฉันจะซื้อมาม่าทุกวันจน จนเหลือเงินน้อยกว่าเท่ากับ 100 บาท")
    print("มาม่า ราคา 100 บาท")
    print("คงเหลือ : " , Money - 100 + 20)
    Money = Money - 100 + 20
else:
    print("ฉันไม่กินล่ะ")